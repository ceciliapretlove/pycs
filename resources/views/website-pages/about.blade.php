                     <h2>Humble Beginnings</h2>
                     <p>In the 1930’s, a considerable number of Chinese migrants from Mainland China found
                        their way to Isabela. Some of them settled in the locality of Cabatuan, Aurora, and
                        Roxas. Cabatuan was then a small, unprogressive barrio of Cauayan. Transportation was
                        very backward and commercial activities were limited. There were only more than ten
                        Chinese families living in these three towns, mostly businessmen engaged in corner sari-
                        sari store and grains trading. They were not rich but deep in their hearts, they were
                        highly nationalists. They loved their ancestral land and culture. They did not want their
                        children to forget their Chinese origin and roots. They wanted this culture to be part of
                        their lives, handed down from generation to generation. It was these common goal and
                        motivation that brought these gentlemen together. The desire to provide their
                        descendants a chance to learn Chinese traditions and values mandated them to establish a
                        school that would serve this purpose. By putting their meager means together, they put
                        up a Chinese school in Cabatuan, Isabela. It was founded in September 1946 by the first
                        Board of Trustees composed of: <strong>Pedro Pua, Mariano Uy, Joaquin Uy,
                        Lorenzo Uy, Miguel Dy, Vicente Pua, Juan Uy, Eusebio Uy, Clemente Paggao.</strong>
                     </p>
                     <p>It was only a rented house affair in front of the San Andres-Del Pilar auditorium with
                        more than thirty (30) learners. They were all accommodated free. Such was the humble
                        beginnings of the school.
                        The school was known as Philippine Chinese Yuh Chiau School for several years, the
                        Chinese name bearing the word “Magat”, named after the famous Magat River coursing
                        along the adjacent towns of Cabatuan, Aurora, and Roxas, also known as the “Magat
                        Area”, where members of the original Board of Trustees came to settle.
                        Under the able management of the Board of Trustees, the school managed to grow
                        and learners increased to hundreds. It necessitated a school building of its own. In 1952,
                        the first building was constructed in Del Pilar. The school subsequently changed its
                        name to Chinese Yuh Chiau School. During the incumbency of the Board of Trustees
                        Chairman Eusebio Uy or Uy Cho Giok, then the youngest member of the Board, he
                        proposed the establishment of a high school department to give access to the elementary
                        graduates for higher education. Cabatuan then, being a poor town, has very few residents
                        who could not afford to send their children to nearby towns or Metro Manila for further
                        studies.
                     </p>
                     <p>Chairman Eusebio Tan, together with the Board of Trustees members, persevered
                        with the high school course despite the problems in additional manpower, facilities,
                        logistics, and finances. In 1955, the English Department obtained a permit from the
                        Bureau of Private Schools. The following year, the Chinese Overseas Commission
                        granted the school the accreditation of the Chinese Department.
                        Thereafter, the student population grew steadily that it necessitated the construction
                        of additional infrastructures to accommodate learners coming from various towns in the
                        province.
                     </p>
                     <p>The declaration of Martial Law in 1972 brought the Filipinization of Chinese Schools
                        all over the land. In view of perceived growing influence and discriminating policies
                        exercised by the schools, the government, by virtue of a law, enforced that all Chinese
                        schools with names bearing the word “Chinese”, must be changed to “Philippine”
                        signifying that said institutions were under the Philippine sovereignty. For this reason
                        that CHINESE YUH CHIAU SCHOOL was changed to <strong>PHILIPPINE YUH CHIAU
                        SCHOOL</strong>. The law further required that members of the Board of Trustees, as well as
                        school officials, must be Filipino citizens.
                     </p>
