                  <h4>Promotion and Retention</h4>
                  <p>The general weighted average or <strong>GWA</strong>. in all learning areas determine the promotion or retention of a
                     learner.
                  </p>
                  <p>A learner is <em>promoted</em> to the next grade level if the GWA of 75% or above is obtained.</p>
                  <p>A learner is <em>retained</em> in the same grade level if there are three (3) learning areas with GWA of lower
                     than 75%.
                  </p>
                  <p>A learner who is <em>retained</em> for two school years may be asked to transfer to another school at the end of
                     the school year.
                  </p>
                  <p><strong>N.B.</strong><br>
                     1. Learners from Grade 1 to Grade 6, Grade 7 to Grade 10 under the Regular Curriculum, and Grades 11 to 12 are
                     required to have a GWA of at least 75% to pass a discipline.<br><br>
                     2. Learners under the Special Science Curriculum shall not have a final rating below 85% in ESM-related
                     subjects, otherwise they are demoted to Regular Curriculum class. Also, a final rating below 80 in any
                     discipline means outright demotion to Regular Curriculum class.
                  </p>
